void OLED_FillScreen(u8 gram[OLED_X_WIDTH][OLED_Y_PAGE])		//整屏刷新（显存数组指针）
{
	int i=0,n=0;
	for(i=0;i<OLED_Y_PAGE;i++)
	{
		OLED_WrCmd(0xb0+i);
		OLED_WrCmd(0x00);
		OLED_WrCmd(0x10);
		for(n=0;n<OLED_X_WIDTH;n++)
		{
			OLED_WrDat(gram[n][i]);
		}
	}	
}


void OLED_P6x8Str(u8 x,u8 y,u8 ch[]) //硬件写字符串:硬件测试
{
	unsigned char c=0,i=0,j=0;      
	while (ch[j]!='\0')
	{    
		c =ch[j]-32;
		if(x>126)
		{
			x=0;
			y++;
		}
		OLED_Set_Pos(x,y);    
		for(i=0;i<6;i++)
		{     
			OLED_WrDat(F6x8[c][i]);  
		}
		x+=6;
		j++;
	}
}

